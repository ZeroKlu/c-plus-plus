#include <iostream>
using namespace std;

int main()
{
    // Escapes for special characters

    // Newline (\n)
    cout << "Before newline\nafter newline" << endl;

    // Tab (\t)
    cout << "\tAfter tab" << endl;

    // Backslash (\\)
    cout << "Backslash \\" << endl;

    // Double-Quote (\")
    cout << "\"Double-quotes\"" << endl;

    // Single-Quote (\')
    cout << "\'Single-Quotes\'" << endl;

    return 0;
}