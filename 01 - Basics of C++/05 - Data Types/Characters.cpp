#include <iostream>
using namespace std;

/*
    Character Types

    The char data type is used to store a single character.
    The character must be surrounded by single quotes, like 'A' or 'c':
 */

int main()
{
    char myGrade = 'B';
    cout << myGrade << endl;

    // Alternatively, you can use ASCII values to display certain characters:
    char a = 97, b = 98, c = 99;
    cout << a << endl;
    cout << b << endl;
    cout << c;

    return 0;
}