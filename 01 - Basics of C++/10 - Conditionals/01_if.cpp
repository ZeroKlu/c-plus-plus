# include <iostream>
using namespace std;

int main()
{
    int x = 20, y = 10;

    // the if control executes the block of code if the boolean expression is true
    if (x == y)
    {
        cout << "x equals y";
    }
    
    if (x > y)
    {
        cout << "x is greater than y";
    }
    
    if (x < y)
    {
        cout << "x is less than y";
    }

    return 0;
}