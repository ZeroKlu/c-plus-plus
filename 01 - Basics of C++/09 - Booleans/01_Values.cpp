#include <iostream>
using namespace std;

int main()
{
    // C++ has a bool data type, which can take the values true (1) or false (0)

    bool isCodingFun = true;
    bool isFishTasty = false;
    cout << isCodingFun << endl;  // Outputs 1 (true)
    cout << isFishTasty << endl;  // Outputs 0 (false)

    return 0;
}